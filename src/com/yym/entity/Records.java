package com.yym.entity;

public class Records {
	private int playA;
	private int playB;
	private int ascore;
	private int bscore;
	private String apic;
	private String bpic;
	public int getPlayA() {
		return playA;
	}
	public void setPlayA(int playA) {
		this.playA = playA;
	}
	public int getPlayB() {
		return playB;
	}
	public void setPlayB(int playB) {
		this.playB = playB;
	}
	public int getAscore() {
		return ascore;
	}
	public void setAscore(int ascore) {
		this.ascore = ascore;
	}
	public int getBscore() {
		return bscore;
	}
	public void setBscore(int bscore) {
		this.bscore = bscore;
	}
	public String getApic() {
		return apic;
	}
	public void setApic(String apic) {
		this.apic = apic;
	}
	public String getBpic() {
		return bpic;
	}
	public void setBpic(String bpic) {
		this.bpic = bpic;
	}
	
}
